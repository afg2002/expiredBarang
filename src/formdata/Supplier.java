    protected void datatable(){
     Object[] Baris = {"id_supplier", "nama_supplier", "alamat", "telepon"};
        tabmode = new DefaultTableModel(null, Baris);
        tblSupplier.setModel(tabmode);

        String sql = "SELECT * FROM supplier";
        try {
            Statement stat = conn.createStatement();
            ResultSet res = stat.executeQuery(sql);
            while (res.next()) {
                String idSupplier = res.getString("id_supplier");
                String namaSupplier = res.getString("nama_supplier");
                String alamat = res.getString("alamat");
                String telepon = res.getString("telepon");

                String[] data = {idSupplier, namaSupplier, alamat, telepon};
                tabmode.addRow(data);
            }
        } catch (SQLException ex) {
            Logger.getLogger(Supplier.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    
