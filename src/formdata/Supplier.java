    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        String idSupplier = txtIdSupplier.getText();
        String namaSupplier = txtNamaSupplier.getText();
        String alamat = txtAlamat.getText();
        String telepon = txtTelepon.getText();

        String[] columns = {"id_supplier", "nama_supplier", "alamat", "telepon"};
        Object[] values = {idSupplier, namaSupplier, alamat, telepon};

        try {
            db.insertData(conn, "supplier", columns, values);
            JOptionPane.showMessageDialog(this, "Supplier berhasil ditambahkan");
            datatable();
            reset();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Gagal menambahkan supplier");
        }
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
         int selectedRowIndex = tblSupplier.getSelectedRow();
            if (selectedRowIndex != -1) {
                String idSupplier = txtIdSupplier.getText();
                String namaSupplier = txtNamaSupplier.getText();
                String alamat = txtAlamat.getText();
                String telepon = txtTelepon.getText();

                String[] columns = {"id_supplier", "nama_supplier", "alamat", "telepon"};
                Object[] values = {idSupplier, namaSupplier, alamat, telepon};
                String condition = "id_supplier = " + idSupplier;

                try {
                    db.updateData(conn, "supplier", columns, values, condition);
                    JOptionPane.showMessageDialog(this, "Supplier berhasil diperbarui");
                    datatable();
                    reset();
                } catch (SQLException ex) {
                    JOptionPane.showMessageDialog(this, "Gagal memperbarui supplier");
                }
            }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
           String idSupplier = txtIdSupplier.getText();

           int confirm = JOptionPane.showConfirmDialog(this, "Apakah Anda yakin ingin menghapus supplier ini?", "Konfirmasi Hapus", JOptionPane.YES_NO_OPTION);
           if (confirm == JOptionPane.YES_OPTION) {
               String condition = "id_supplier = " + idSupplier;

               try {
                   db.deleteData(conn, "supplier", condition);
                   reset();
                   datatable();
                   JOptionPane.showMessageDialog(this, "Supplier berhasil dihapus");
               } catch (SQLException ex) {
                   JOptionPane.showMessageDialog(this, "Gagal menghapus supplier");
               }
           }
    }//GEN-LAST:event_btnDeleteActionPerformed

